from fractions import Fraction

# Create Fraction objects
frac1 = Fraction(1, 3)
frac2 = Fraction(2, 5)

# Perform fractional arithmetic
addition_result = frac1 + frac2
subtraction_result = frac1 - frac2
multiplication_result = frac1 * frac2
division_result = frac1 / frac2

# Display the results
print(f"Fraction 1: {frac1}")
print(f"Fraction 2: {frac2}")
print(f"Addition: {addition_result}")
print(f"Subtraction: {subtraction_result}")
print(f"Multiplication: {multiplication_result}")
print(f"Division: {division_result}")
